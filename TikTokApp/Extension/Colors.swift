import UIKit

extension UIColor {
    
    public class var backgroundBarAppearance: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 1)
    }
    
}

extension CGColor {
    
    public class var gray: CGColor {
        return CGColor(red: 217/255, green: 217/255, blue: 217/255, alpha: 0.8)
    }
}
